#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn space_is_blank_and_lowercase_folds() {
        assert_eq!(glyph(' '), SPACE);
        assert_eq!(glyph('a'), glyph('A'));
    }

    #[test]
    fn unknown_uses_missing_box() {
        assert_eq!(glyph('\u{3042}'), MISSING); // あ — not in the placeholder font yet
    }
}
